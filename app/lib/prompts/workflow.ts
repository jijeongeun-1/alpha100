/**
 * 산출물 유형명에서 해당하는 워크플로우 섹션을 반환합니다.
 */
export function getRelevantWorkflows(deliverables: string[]): string {
  const text = deliverables.join(' ').toLowerCase()

  const selected: string[] = []

  if (/로고|logo|ci|bi/.test(text)) selected.push('[A]')
  if (/패키지|package|박스|포장/.test(text)) selected.push('[B]')
  if (/카탈로그|catalogue|catalog|브로슈어|brochure|리플렛|leaflet|인쇄|책자|소책자/.test(text)) selected.push('[C]')
  if (/상세페이지|상세 페이지|sns|콘텐츠|content|배너|banner|카드뉴스/.test(text)) selected.push('[D]')
  if (/앱|어플|app|자체개발|웹\s*개발|앱\s*개발|개발|플랫폼/.test(text)) selected.push('[E]')
  if (/홈페이지|웹사이트|아임웹|카페24|워드프레스|웹빌더|솔루션|사이트/.test(text)) selected.push('[F]')
  // UXUI는 자체개발·솔루션 모두 해당 → 양쪽 미포함 시 각각 추가
  if (/uxui|ux\/ui|ui\/ux/.test(text)) {
    if (!selected.includes('[E]')) selected.push('[E]')
    if (!selected.includes('[F]')) selected.push('[F]')
  }

  if (selected.length === 0) return ''

  const separator = '━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━'
  const parts = WORKFLOW_REFERENCE.split(separator)
  // parts[0] = intro, parts[1] = [A] title, parts[2] = A content, parts[3] = [B] title, ...
  const header = parts[0] + separator

  const matchedParts: string[] = []
  for (let i = 1; i < parts.length; i += 2) {
    const sectionLabel = parts[i]?.trimStart().slice(0, 4) ?? ''
    if (selected.some((tag) => sectionLabel.startsWith(tag))) {
      matchedParts.push(parts[i])
      if (i + 1 < parts.length) matchedParts.push(parts[i + 1])
    }
  }

  return header + matchedParts.join(separator)
}
